package day12_strinManipulation;

public class C10_forLoop {
    public static void main(String[] args) {

        //Jaca'da baslangıc ve bitiş degeri belli olan veya tekrar
        // sayısı verilmiş olan işlemler için
        //LOOP kullanırız
        // 5 kere Hello World yazdırın

        String str= "Hello World\n";

        System.out.println(str.repeat(5));

        // 1'den 10'a kadar olan sayilari tek tek yazdırın

        for(int i=1; i<=10; i++){
            System.out.println(i+" ");

        }




    }
}
